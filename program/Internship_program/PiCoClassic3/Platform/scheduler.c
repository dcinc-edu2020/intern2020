/* ソフトウェアのタスクを実行する						*/
/* ---------------------------------------------------------------------------- */
/*  Arguments:		-							*/
/*  Return   :         	-							*/
/*==============================================================================*/
void task_routine(void)
{
	/* まずは、スタート位置から0.5区画進む */
	run_straight(HALF_SECTION, DEFAULT_SPEED);	// 座標(0,1)へ進む
	
	/* 判断 */
	judgement();	// 走行経路を判断する
		
	/* 制御 */
	control();	// 走行経路に従って走行制御する
	
	/* ゴール */
	run_straight(HALF_SECTION, MIN_SPEED);		// 0.5区画直進する（ゴールに向かって減速する）
	
	return;
}