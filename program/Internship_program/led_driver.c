/*------------------------------------------------------------------------------*/
/* Function prototype								*/
/*------------------------------------------------------------------------------*/
void vd_DisplayLed(int iLed_mode);	// モード表示(LED)

/*------------------------------------------------------------------------------*/
/* Function Definitions								*/
/*------------------------------------------------------------------------------*/

/*==============================================================================*/
/* モード表示(LED)					                        */
/* 										*/
/* 現在のモードを車体前方のLEDに表示する					*/
/* ---------------------------------------------------------------------------- */
/*  Arguments:		iLed_mode	1〜15までのモード情報			*/
/*  Return   :         	-							*/
/*==============================================================================*/
void vd_DisplayLed(int iLed_mode)
{
	LED0 = iLed_mode & 0x01;
	LED1 = (iLed_mode >> 1) & 0x01;
	LED2 = (iLed_mode >> 2) & 0x01;
	LED3 = (iLed_mode >> 3) & 0x01;
}