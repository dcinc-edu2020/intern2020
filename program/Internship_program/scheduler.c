void vd_RunLefthand(void);	// 左手法走行

/*------------------------------------------------------------------------------*/
/* Function Definitions								*/
/*------------------------------------------------------------------------------*/

/*==============================================================================*/
/* 左手法走行						                        */
/* 										*/
/* 左手法に従って走行する							*/
/* ---------------------------------------------------------------------------- */
/*  Arguments:		led_mode	1〜15までのモード情報			*/
/*  Return   :         	-							*/
/*==============================================================================*/
void vd_RunLefthand(void)
{
	/* 加速度設定 */
	accel = SEARCH_ACCEL;
	
	/* スタート位置から0.5区画進む */
	vd_RunStraight(STRAIGHT_HALFSECTION, SEARCH_SPEED);
	
	/* 以降、1区画ごとに壁の有無を判定しながら空いている方向へ進む */
	while(1)
	{
		vd_Judgement();
	}	
}